void predict(union Entry* data, int pred_margin, float* result) {
  unsigned int tmp;
  if ( ( !(data[4].missing != -1) || ((*( ((int*)(data)) + 4 ))<=((int)(0x41bc0000))) ) ) {
    result[0] += 0.039104024297646166;
    result[1] += 0.0018982536066818527;
    result[2] += 0.004176157934700076;
    result[3] += 0.48120728929384965;
    result[4] += 0.47228549734244496;
    result[5] += 0.0013287775246772968;
    result[6] += 0;
  } else {
    result[0] += 0.8921845574387948;
    result[1] += 0.0004969658924461185;
    result[2] += 0.002720234358652438;
    result[3] += 0.1042843691148776;
    result[4] += 0;
    result[5] += 2.6156099602427287e-05;
    result[6] += 0.00028771709562670013;
  }
  
  // Average tree outputs
  result[0] /= 1;
  result[1] /= 1;
  result[2] /= 1;
  result[3] /= 1;
  result[4] /= 1;
  result[5] /= 1;
  result[6] /= 1;
  
  // Apply base_scores
  result[0] += 0;
  result[1] += 0;
  result[2] += 0;
  result[3] += 0;
  result[4] += 0;
  result[5] += 0;
  result[6] += 0;
  
  // Apply postprocessor
  if (!pred_margin) { postprocess(result); }
}

